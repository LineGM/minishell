#include "libft.h"

/*
 * Function that converts an uppercase letter 'c' to lowercase. If 'c' is no
 * uppercase letter, 'c' is returned unchanged.
 */
int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c += 32;
	return (c);
}
