#include "libft.h"

/*
 * Function that uses malloc to allocate memory for 'count' elements of size
 * 'size' and sets this memory to zero.
 */
void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = malloc(count * size);
	if (ptr == NULL)
		return (NULL);
	ft_bzero(ptr, count * size);
	return (ptr);
}
