#include "libft.h"

/*
 * Function that returns 1 if 'c' is an alphabetic character
 * (uppercase or lowercase) and 0 if it isn't.
 */
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
