#include "libft.h"

/*
 * Function that returns the length of the string 'str', excluding the
 * terminating null byte.
 */
size_t	ft_strlen(const char *str)
{
	size_t	length;

	length = 0;
	while (str[length] != '\0')
		length++;
	return (length);
}
